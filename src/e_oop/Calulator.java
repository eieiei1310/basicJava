package e_oop;

public class Calulator {

	double plus(double a, double b){
		return a + b;
	}
	
	double minus(double a, double b){
		return a - b;
	}
	
	double multiply(double a, double b){
		return a * b;
	}
	
	double division(double a, double b){
		return a / b;
	}
	
	double remainder(double a, double b){
		return a % b;
	}
	
	
	
	
}
